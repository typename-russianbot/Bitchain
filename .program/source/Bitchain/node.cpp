//? @defgroup: Resources
//////////////////////////////////////////////////////
//* @public: Node(const Key&)
Node::Node(const Key &nKey) : key(nKey), next(nullptr), prev(nullptr) { return; }

//* @public: Node(const Node&)
Node::Node(const Node &node) : key(node.key), next(node.next), prev(node.prev) { return; }
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: ~Node(void)
Node::~Node(void) { return; }
//////////////////////////////////////////////////////

//? @defgroup: Mutators
//////////////////////////////////////////////////////
//* @public: setKey(const Key&)
void Node::setKey(const Key &key)
{
    this->key = key;
    return;
}
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: setNext(Node*)
void Node::setNext(Node *node)
{
    this->next = node;
    return;
}
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: setPrev(Node*)
void Node::setPrev(Node *node)
{
    this->prev = node;
    return;
}
//////////////////////////////////////////////////////

//? @defgroup: Accessors
//////////////////////////////////////////////////////
//* @public: getKey(void)
Key &Node::getKey(void) { return key; }
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: getNext(void)
Node *Node::getNext(void) { return next; }
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: getPrev(void)
Node *Node::getPrev(void) { return prev; }
//////////////////////////////////////////////////////

//? @defgroup: Overloads
//////////////////////////////////////////////////////
//* @public: operator=(const Node&)
Node &Node::operator=(const Node &other)
{
    this->key = other.key;
    this->next = other.next;
    this->prev = other.prev;
    
    return *this;
}
