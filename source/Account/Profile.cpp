//* @defgroup: Mutators/Accessors
//////////////////////////////////////////////////////
//* @public: setUsername(const string)
void Profile::setUsername(const string nUsername)
{
    username = nUsername;
    return;
}
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: setPassword(const string)
void Profile::setPassword(const string nPassword)
{
    password = nPassword;
    return;
}
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: getUsername(void)
const string Profile::getUsername(void)
{
    return username;
}
//////////////////////////////////////////////////////
//////////////////////////////////////////////////////
//* @public: getPassword(void)
